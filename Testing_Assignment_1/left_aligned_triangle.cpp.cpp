//Description: This C++ code prints a left-aligned triangle pattern using asterisks (*) on the console. 
//The pattern starts with one asterisk on the first line and adds one more asterisk to each subsequent 
//line, creating a left-aligned triangle. The output will look like a right-angled triangle that is 
//aligned to the left.
using namespace std;
#include <iostream>
int main(){
	
	cout <<"         *"<<"\n"<<"        **"<<"\n"<<"       ***"<<"\n"<<"      ****"<<"\n"<<"     *****"<<"\n"<<"    ******"<<"\n"<<"   *******"<<"\n"<<"  ********"<<"\n"<<" *********"<<"\n"<<"**********";
	
	return 0;
}
