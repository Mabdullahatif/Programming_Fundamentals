//Description: This C++ code prints a simple triangle pattern using asterisks (*) on the console. 
//The pattern starts with one asterisk on the first line and adds one more asterisk to each subsequent 
//line until it reaches ten asterisks on the last line. The output will look like a right-angled triangle.
using namespace std;
#include <iostream>
